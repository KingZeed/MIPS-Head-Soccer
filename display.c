/* From lab */
void display_string(int line, char *s) {
	int i;
	if(line < 0 || line >= 4)
		return;
	if(!s)
		return;
	
	for(i = 0; i < 16; i++)
		if(*s) {
			textbuffer[line][i] = *s;
			s++;
		} else
			textbuffer[line][i] = ' ';
}

/* From lab */
void string_update() {
	int i, j, k;
	int c;
	for(i = 0; i < 4; i++) {
		DISPLAY_COMMAND_DATA_PORT &= ~DISPLAY_COMMAND_DATA_MASK;
		spi_send_recv(0x22);
		spi_send_recv(i);
		
		spi_send_recv(0x0);
		spi_send_recv(0x10);
		
		DISPLAY_COMMAND_DATA_PORT |= DISPLAY_COMMAND_DATA_MASK;
		
		for(j = 0; j < 16; j++) {
			c = textbuffer[i][j];
			if(c & 0x80)
				continue;
			
			for(k = 0; k < 8; k++)
				spi_send_recv(font[c*8 + k]);
		}
	}
}

/* From lab */
void delay(int cyc) {
	int i;
	for(i = cyc; i > 0; i--);
}

/* From lab */
void spi_send_recv(uint8_t data) {
	while(!(SPI2STAT & 0x08));
	SPI2BUF = data;
	while(!(SPI2STAT & 0x01));
	return SPI2BUF;
}

/* From lab */
void display_init() {	 
	DISPLAY_COMMAND_DATA_PORT &= ~DISPLAY_COMMAND_DATA_MASK; // Clear OLED data/command select
	delay(10);
	DISPLAY_VDD_PORT &= ~DISPLAY_VDD_MASK;					// Clear OLED VDD Enable - Power off display
	delay(1000000);
	
	// Send display off command
	spi_send_recv(0xAE);

	// Reset the display
	DISPLAY_RESET_PORT &= ~DISPLAY_RESET_MASK;
	delay(10);
	DISPLAY_RESET_PORT |= DISPLAY_RESET_MASK;
	delay(10);
	
	// Set pre-charge phase length
	spi_send_recv(0x8D);
	spi_send_recv(0x14);
	
	// Set VCOMH deselect level
	spi_send_recv(0xD9);
	spi_send_recv(0xF1);
	
	// Power on the display
	DISPLAY_VBATT_PORT &= ~DISPLAY_VBATT_MASK;
	delay(10000000);
	
	// Set segment remap and com output scan direction
	spi_send_recv(0xA1);
	spi_send_recv(0xC8);
	
	// Set COM pins hardware configuration
	spi_send_recv(0xDA);
	spi_send_recv(0x20);

	// Turn on the display
	spi_send_recv(0xAF);
}

/* From lab */
void display_image(const uint8_t *data) {
	int i, j;
	
	for(i = 0; i < 4; i++) {
		DISPLAY_COMMAND_DATA_PORT &= ~DISPLAY_COMMAND_DATA_MASK;
		spi_send_recv(0x22);
		spi_send_recv(i);
		
		spi_send_recv(0 & 0xF);
		spi_send_recv(0x10 | ((0 >> 4) & 0xF));
		
		DISPLAY_COMMAND_DATA_PORT |= DISPLAY_COMMAND_DATA_MASK;
		
		for(j = 0; j < DISPLAY_LENGTH; j++)
			spi_send_recv(data[i*DISPLAY_LENGTH + j]);
	}
	
}

/* Translate the display coordinate system to the oled_data */
void translate_to_oled(){

	int page, column, memory_row;
	uint8_t oled_byte = 0; // The byte we will add to oled_data
	
	// Iterate through the display coordinate system
	// The oled_data will be read through page 0 - 3
	for(page = 0; page < 4; page++){

		for(column = 0; column < DISPLAY_LENGTH; column++) {
			oled_byte = 0;

			for(memory_row = 0; memory_row < 8; memory_row++){
				if(display[8 * page + memory_row][column]){
					oled_byte |= (1 << memory_row); // change the byte everytime a pixel is on
				}
				
			}
			oled_data[page*DISPLAY_LENGTH + column] = oled_byte;
		}
		
	}

}

/* Makes the screen black */
void display_clear() {
    int row, column, i;

	for(row = 0; row < DISPLAY_HEIGHT; row++) {
		for(column = 0; column < DISPLAY_LENGTH; column++) {
		display[row][column] = 0;
		}
	}
	translate_to_oled();
	display_image(oled_data);
}

void display_update(){
	translate_to_oled();
	display_image(oled_data);
}

void display_dots() {
	
	display[0][0] = 1;
	display[31][127] = 1;
	display[31][0] = 1;
	display[0][127] = 1;
	
	
	display_update();
	
}


